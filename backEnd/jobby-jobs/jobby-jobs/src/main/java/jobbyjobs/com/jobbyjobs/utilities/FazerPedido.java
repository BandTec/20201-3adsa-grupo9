package jobbyjobs.com.jobbyjobs.utilities;

public class FazerPedido {

    private double valorTotal;
    private int qtdHorasTrabalho;
    private int idUsuario;
    private int idBaba;

    public double getValorTotal() {
        return valorTotal;
    }
    public void setValorTotal(double valorTotal) {
        this.valorTotal = valorTotal;
    }

    public int getQtdHorasTrabalho() {
        return qtdHorasTrabalho;
    }
    public void setQtdHorasTrabalho(int qtdHorasTrabalho) {
        this.qtdHorasTrabalho = qtdHorasTrabalho;
    }

    public int getIdUsuario() {
        return idUsuario;
    }
    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    public int getIdBaba() {
        return idBaba;
    }
    public void setIdBaba(int idBaba) {
        this.idBaba = idBaba;
    }
}
