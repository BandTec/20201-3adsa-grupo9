package jobbyjobs.com.jobbyjobs.utilities;

public class CalculoOrcamento {

    private int qtdCriancasZeroAteTres;
    private int qtdCriancasTresAteOito;
    private int qtdCriancasNoveAteQuinze;
    private int qtdHoras;

    public int getQtdCriancasZeroAteTres() {
        return qtdCriancasZeroAteTres;
    }
    public void setQtdCriancasZeroAteTres(int qtdCriancasZeroAteTres) {
        this.qtdCriancasZeroAteTres = qtdCriancasZeroAteTres;
    }

    public int getQtdCriancasTresAteOito() {
        return qtdCriancasTresAteOito;
    }
    public void setQtdCriancasTresAteOito(int qtdCriancasTresAteOito) {
        this.qtdCriancasTresAteOito = qtdCriancasTresAteOito;
    }

    public int getQtdCriancasNoveAteQuinze() {
        return qtdCriancasNoveAteQuinze;
    }
    public void setQtdCriancasNoveAteQuinze(int qtdCriancasNoveAteQuinze) {
        this.qtdCriancasNoveAteQuinze = qtdCriancasNoveAteQuinze;
    }

    public int getQtdHoras() {
        return qtdHoras;
    }
    public void setQtdHoras(int qtdHoras) {
        this.qtdHoras = qtdHoras;
    }
}
